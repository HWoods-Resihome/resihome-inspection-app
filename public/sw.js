/*
 * ResiWalk service worker — gives the field app a reliable offline shell.
 *
 * Without this, reloading the page in a dead zone (or after the OS evicts a
 * backgrounded tab) shows a blank screen. Strategy:
 *   - Navigations: network-first, fall back to the last cached page, then to a
 *     cached shell. So inspectors always get *something* offline.
 *   - Static build assets (/_next/static, icons, css, js, fonts, images):
 *     stale-while-revalidate, so they load instantly and refresh in the
 *     background — new deploys are picked up without an SW bump.
 *   - API calls (/api/*): never cached. The app has its own offline outbox /
 *     IndexedDB queues; caching API responses would serve stale data.
 */

const NAV_FALLBACK = '/';
// Cache name is tied to the build version passed in the registration URL
// (/sw.js?v=<build>), so every deploy gets a FRESH cache and the activate
// handler below deletes all older ones — no more stale shells surviving a
// deploy. Falls back to a static name when registered without a version.
const SW_VERSION = (() => {
  try { return new URL(self.location.href).searchParams.get('v') || 'v3'; }
  catch { return 'v3'; }
})();
const CACHE = 'resiwalk-shell-' + SW_VERSION;
// Photos (via /api/photo-proxy) are immutable per URL and version-independent, so
// they live in their OWN cache that survives deploys (NOT wiped in activate). This
// is what stops a photo-heavy scope from re-fetching the same tiles hundreds of
// times over the network — the request storm that trips Vercel's DDoS challenge.
const PHOTO_CACHE = 'resiwalk-photos';
const PHOTO_CACHE_MAX = 800; // soft LRU cap (thumbnails are small; a few MB total)

// The FULL list of this build's JS/CSS chunks (generated by
// scripts/sw-precache.mjs → public/sw-precache.js). Loaded via importScripts so
// it rides the public `.js` static path (no auth gate) and is available
// synchronously at install. Empty in dev / if the file is absent — tolerated.
let PRECACHE_CHUNKS = [];
try {
  importScripts('/sw-precache.js?v=' + SW_VERSION);
  if (Array.isArray(self.__SW_PRECACHE)) PRECACHE_CHUNKS = self.__SW_PRECACHE;
} catch (e) { /* not generated (dev) / offline at install — SWR still warms later */ }

// Brand assets that must ALWAYS render offline (e.g. the header logo). The
// stale-while-revalidate handler only caches an asset AFTER it's fetched once,
// and a new deploy wipes the prior cache (see activate) — so right after a
// deploy, going offline before these were re-fetched left a broken logo. Pull
// them into the fresh cache at install time so that can't happen. Tolerant: a
// missing/renamed file never fails the whole install.
const PRECACHE_ASSETS = ['/app-icon.svg', '/icon-192.png', '/icon-512.png', '/'];

self.addEventListener('install', (event) => {
  // Activate immediately. The SW's bytes only change when the page that
  // registers it loads with a new /sw.js?v=<build> — i.e. AFTER the user has
  // already reloaded onto the new bundle. skipWaiting + clients.claim (in
  // activate) means the fresh SW takes over in that same load.
  self.skipWaiting();
  event.waitUntil((async () => {
    try {
      const cache = await caches.open(CACHE);
      await Promise.all(PRECACHE_ASSETS.map(async (url) => {
        try {
          const res = await fetch(url, { cache: 'no-store' });
          // !redirected: the SW installs while on /login (first-ever install, or a
          // session-expiry bounce), where these authed routes 302→/login. Without
          // this guard the LOGIN page HTML gets cached under the offline shell keys
          // ('/', etc.), so an offline reload later shows login despite a valid
          // session. Mirrors the navigation handler's guard.
          if (res && res.status === 200 && !res.redirected) await cache.put(url, res.clone());
        } catch { /* offline at install / missing asset — non-fatal */ }
      }));
      // Precache the FULL JS/CSS bundle for this build (PRECACHE_CHUNKS, loaded
      // via importScripts above) so every route AND the lazily-loaded
      // (next/dynamic) inspection forms render OFFLINE — including on a cold start.
      // Without this the inspection page mounts offline but its form chunk can't be
      // fetched and hangs on the loading spinner. Tolerant: a missing asset never
      // fails the install (we cache what we can; the rest falls back to network).
      // Retry each chunk a couple times — a single dropped request on a flaky
      // connection during install used to leave the new cache incomplete (old
      // cache is purged on activate), so a form chunk could be missing and the
      // inspection hung on "Loading…" offline. The chunks are immutable + usually
      // already in the browser's HTTP cache from the page that registered us, so
      // this is fast and almost always succeeds first try.
      const cacheWithRetry = async (url, attempts = 3) => {
        for (let i = 0; i < attempts; i++) {
          try { const r = await fetch(url); if (r && r.status === 200) { await cache.put(url, r.clone()); return; } }
          catch { /* retry */ }
          await new Promise((res) => setTimeout(res, 400 * (i + 1)));
        }
      };
      await Promise.all(PRECACHE_CHUNKS.map((url) => cacheWithRetry(url)));
      // Cache id-AGNOSTIC route shells so an OFFLINE hard navigation / reload /
      // deep-link to a dynamic inspection URL (/inspection/<id>) renders the app
      // instead of the dead "Offline" page. The [id] page has no per-id server
      // data — the client reads the id from the URL — so ONE shell (fetched for a
      // throwaway id) serves every inspection, including offline-created local_*
      // ones. Stored under a stable cache key the navigate handler looks up.
      await Promise.all([
        ['/inspection/__id_shell__', '/inspection/_precache_shell_'],
        ['/inspection/new', '/inspection/new'],
      ].map(async ([key, url]) => {
        try { const r = await fetch(url, { cache: 'no-store' }); if (r && r.ok && !r.redirected) await cache.put(key, r.clone()); }
        catch { /* offline at install — non-fatal */ }
      }));
    } catch { /* cache open failed — non-fatal */ }
  })());
});

self.addEventListener('activate', (event) => {
  event.waitUntil(
    (async () => {
      const keys = await caches.keys();
      // Keep the current shell cache AND the persistent photo cache; wipe the rest.
      await Promise.all(keys.filter((k) => k !== CACHE && k !== PHOTO_CACHE).map((k) => caches.delete(k)));
      await self.clients.claim();
    })(),
  );
});

self.addEventListener('message', (event) => {
  if (event.data === 'SKIP_WAITING') self.skipWaiting();
});

/* ------------------------------------------------------------------
 * Web Push — approval alerts (and any future server-sent notification).
 * The server (lib/pushSender) sends a JSON body { title, body, url, tag }.
 * Tapping the notification focuses an open tab (navigating it if needed) or
 * opens a new one at `url`.
 * ------------------------------------------------------------------ */
self.addEventListener('push', (event) => {
  let data = {};
  try { data = event.data ? event.data.json() : {}; } catch (_) { /* non-JSON */ }
  const title = data.title || 'ResiWalk';
  const options = {
    body: data.body || '',
    icon: '/icon-192.png',
    badge: '/icon-192.png',
    tag: data.tag || undefined,
    data: { url: data.url || '/' },
  };
  event.waitUntil(self.registration.showNotification(title, options));
});

self.addEventListener('notificationclick', (event) => {
  event.notification.close();
  const target = (event.notification.data && event.notification.data.url) || '/';
  event.waitUntil((async () => {
    const all = await self.clients.matchAll({ type: 'window', includeUncontrolled: true });
    for (const c of all) {
      // Focus an existing tab; navigate it to the target if it supports it.
      if ('focus' in c) {
        try { if (c.url !== target && 'navigate' in c) await c.navigate(target); } catch (_) { /* cross-origin guard */ }
        return c.focus();
      }
    }
    if (self.clients.openWindow) return self.clients.openWindow(target);
  })());
});

/* ------------------------------------------------------------------ *
 * Background Sync: upload queued photos even after the tab is closed.
 *
 * Queued captures live in IndexedDB (resiwalk_photos/queue), written by
 * lib/offlinePhotoStore.ts. When connectivity returns the browser fires this
 * `sync` event (Chromium; iOS Safari has no Background Sync, where it's a
 * no-op and the in-app flush handles it).
 *
 * If a window is open we let the page run its normal upload+attach flow (it
 * has the form context to attach URLs to sections/lines). Only when no window
 * is open do we upload blobs here, recording the resulting HubSpot URL back on
 * the record so the cheap attach step completes next time the app opens.
 * Throwing makes the browser retry the sync later.
 * ------------------------------------------------------------------ */
const PHOTO_DB = 'resiwalk_photos';
const PHOTO_STORE = 'queue';
const PHOTO_SYNC_TAG = 'resiwalk-photo-sync';

self.addEventListener('sync', (event) => {
  if (event.tag === PHOTO_SYNC_TAG) event.waitUntil(handlePhotoSync());
});

async function handlePhotoSync() {
  const clients = await self.clients.matchAll({ type: 'window', includeUncontrolled: true });
  if (clients.length > 0) {
    // A tab is open — it can do the full upload AND attach correctly.
    for (const c of clients) c.postMessage({ type: 'resiwalk-flush' });
    return;
  }
  await uploadQueuedPhotosInBackground();
}

function idbOpen() {
  return new Promise((resolve, reject) => {
    const req = indexedDB.open(PHOTO_DB, 1);
    // Don't create the store here — if it doesn't exist there's nothing to sync.
    req.onsuccess = () => resolve(req.result);
    req.onerror = () => reject(req.error);
  });
}

function idbGetAll(db) {
  return new Promise((resolve, reject) => {
    if (!db.objectStoreNames.contains(PHOTO_STORE)) return resolve([]);
    const t = db.transaction(PHOTO_STORE, 'readonly');
    const req = t.objectStore(PHOTO_STORE).getAll();
    req.onsuccess = () => resolve(req.result || []);
    req.onerror = () => reject(req.error);
  });
}

function idbPut(db, rec) {
  return new Promise((resolve, reject) => {
    const t = db.transaction(PHOTO_STORE, 'readwrite');
    t.objectStore(PHOTO_STORE).put(rec);
    t.oncomplete = () => resolve();
    t.onerror = () => reject(t.error);
  });
}

function bufToBase64(buffer) {
  // Chunked to avoid a call-stack overflow on large arrays.
  const bytes = new Uint8Array(buffer);
  let binary = '';
  const CHUNK = 0x8000;
  for (let i = 0; i < bytes.length; i += CHUNK) {
    binary += String.fromCharCode.apply(null, bytes.subarray(i, i + CHUNK));
  }
  return btoa(binary);
}

// Cap per-photo background attempts so a single un-uploadable image (corrupt
// blob, a file the server keeps rejecting) can't keep failing the sync forever.
const MAX_BG_PHOTO_ATTEMPTS = 6;

async function uploadQueuedPhotosInBackground() {
  let db;
  try { db = await idbOpen(); } catch { return; }
  const all = await idbGetAll(db);
  // Photos only — video uses a Vercel Blob client flow that can't run here; it
  // syncs via the foreground flush when the app reopens. Skip photos that have
  // exhausted their background attempts (they'll be retried by the foreground
  // flush when the app reopens, which surfaces errors to the user).
  // Records now store durable ArrayBuffer bytes (`bytes`); older records may
  // still carry a Blob (`blob`). Accept either; skip ones with neither.
  const pending = all
    .filter((r) => r && r.kind === 'photo' && !r.uploadedUrl && ((r.bytes && r.bytes.byteLength) || (r.blob && r.blob.size)) && (r.bgAttempts || 0) < MAX_BG_PHOTO_ATTEMPTS)
    .sort((a, b) => a.createdAt - b.createdAt);

  let hadRetryableFailure = false;
  for (const rec of pending) {
    let res;
    try {
      const buf = (rec.bytes && rec.bytes.byteLength) ? rec.bytes : await rec.blob.arrayBuffer();
      const base64 = bufToBase64(buf);
      res = await fetch('/api/upload', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ filename: rec.filename, contentType: 'image/jpeg', base64 }),
      });
    } catch (e) {
      // Network/offline — every upload will fail the same way; stop and let the
      // browser reschedule the whole sync. Records are left intact.
      throw (e instanceof Error ? e : new Error('background upload network error'));
    }
    if (res.status === 401 || res.status === 403) {
      // Session expired — every upload will fail; stop and let the browser retry
      // the whole sync once the user re-authenticates.
      throw new Error(`background upload auth failed: HTTP ${res.status}`);
    }
    if (!res.ok) {
      // Per-photo server error (e.g. one bad file): record the attempt and SKIP
      // to the next photo so one poison-pill image can't block the rest of the
      // queue. The browser will reschedule (we throw at the end).
      rec.bgAttempts = (rec.bgAttempts || 0) + 1;
      try { await idbPut(db, rec); } catch { /* ignore */ }
      hadRetryableFailure = true;
      continue;
    }
    const data = await res.json().catch(() => ({}));
    if (data && data.url) {
      rec.uploadedUrl = data.url;
      rec.bgAttempts = 0;
      await idbPut(db, rec);
    }
  }
  // If anything failed transiently, throw so the browser reschedules the sync.
  // Already-uploaded photos carry uploadedUrl and won't be retried.
  if (hadRetryableFailure) throw new Error('one or more background photo uploads failed; will retry');
}

function isStaticAsset(url) {
  return (
    url.pathname.startsWith('/_next/static/') ||
    /\.(?:js|css|woff2?|ttf|otf|png|jpe?g|svg|gif|webp|ico)$/i.test(url.pathname)
  );
}

// Soft LRU trim for the photo cache: CacheStorage keys() is insertion-ordered, so
// deleting from the front drops the oldest entries once we exceed the cap.
async function trimPhotoCache(cache) {
  try {
    const keys = await cache.keys();
    if (keys.length <= PHOTO_CACHE_MAX) return;
    const excess = keys.length - PHOTO_CACHE_MAX;
    for (let i = 0; i < excess; i++) await cache.delete(keys[i]);
  } catch { /* quota/read error — non-fatal */ }
}

self.addEventListener('fetch', (event) => {
  const req = event.request;
  if (req.method !== 'GET') return;

  const url = new URL(req.url);
  if (url.origin !== self.location.origin) return; // leave cross-origin alone
  // Photos: CacheFirst in the persistent photo cache. A proxied photo's bytes are
  // immutable per URL, so once cached we serve it WITHOUT touching the network —
  // repeat renders / re-scrolls of a photo-heavy scope no longer hammer
  // /api/photo-proxy (the volume that trips Vercel's DDoS challenge and blocks
  // fetch()-based calls like AI review + autosave). ignoreVary so a stored WebP
  // still serves when the Accept header shifts.
  if (url.pathname === '/api/photo-proxy') {
    event.respondWith((async () => {
      const cache = await caches.open(PHOTO_CACHE);
      const hit = await cache.match(req, { ignoreVary: true });
      if (hit) return hit;
      try {
        const res = await fetch(req);
        if (res && res.status === 200 && !res.redirected) {
          cache.put(req, res.clone()).then(() => trimPhotoCache(cache)).catch(() => {});
        }
        return res;
      } catch {
        const any = await cache.match(req, { ignoreVary: true });
        return any || Response.error();
      }
    })());
    return;
  }
  if (url.pathname.startsWith('/api/')) return;     // never cache other API
  // NEVER intercept the manifest or the SW itself — they must always come
  // straight from the network. A cached/stale (or HTML) response here breaks
  // Chrome's installability check (manifest must parse as JSON).
  if (url.pathname === '/manifest.webmanifest' || url.pathname === '/sw.js') return;

  // Navigations: network-first with a cached fallback so offline reloads work.
  if (req.mode === 'navigate') {
    event.respondWith(
      (async () => {
        try {
          // no-store: bypass the HTTP cache so a reload after a deploy ALWAYS gets
          // the fresh HTML (which references the new hashed chunks) in one go.
          const fresh = await fetch(req, { cache: 'no-store' });
          // Only cache a real, non-redirected page. A session-expiry 302→/login or
          // a 5xx error page (fetch follows redirects) must NOT be cached under the
          // requested route URL, or an offline reload would serve the login/error
          // page instead of the inspection shell until a later online nav overwrites it.
          if (fresh.ok && !fresh.redirected) {
            const cache = await caches.open(CACHE);
            cache.put(req, fresh.clone());
          }
          return fresh;
        } catch {
          const cache = await caches.open(CACHE);
          // Exact match first (home, /inspection/new, a previously-visited
          // inspection URL).
          const exact = await cache.match(req);
          if (exact) return exact;
          // OFFLINE deep-link / reload / hard-nav to a dynamic inspection URL:
          // serve the id-agnostic [id] shell so the SPA boots and client-renders
          // the inspection (it reads the id from window.location) — instead of the
          // dead "Offline" page. This is what made an offline-created inspection
          // un-openable after a hard navigation.
          if (url.pathname.startsWith('/inspection/') && url.pathname !== '/inspection/new') {
            // a) the id-agnostic shell the page cached for us, or
            const shell = await cache.match('/inspection/__id_shell__');
            if (shell) return shell;
            // b) ANY previously-visited inspection's cached HTML — the [id] page
            //    is id-agnostic (reads the id from the URL), so any one works as
            //    the shell. Covers users who opened a real inspection online even
            //    if the explicit shell wasn't cached.
            const keys = await cache.keys();
            for (const k of keys) {
              const p = new URL(k.url).pathname;
              if (/^\/inspection\/[^/]+\/?$/.test(p) && p !== '/inspection/new') {
                const hit = await cache.match(k);
                if (hit) return hit;
              }
            }
          }
          return (await cache.match(NAV_FALLBACK)) ||
            new Response('<h1>Offline</h1><p>Reconnect to load this page. Your saved work is safe on this device.</p>', { headers: { 'Content-Type': 'text/html' } });
        }
      })(),
    );
    return;
  }

  // Static assets: stale-while-revalidate.
  if (isStaticAsset(url)) {
    event.respondWith(
      (async () => {
        const cache = await caches.open(CACHE);
        const cached = await cache.match(req);
        const network = fetch(req).then((res) => {
          if (res && res.status === 200) cache.put(req, res.clone());
          return res;
        }).catch(() => undefined);
        return cached || (await network) || new Response('', { status: 504 });
      })(),
    );
  }
});
